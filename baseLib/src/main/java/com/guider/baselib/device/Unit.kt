package com.guider.baselib.device

/**
 * 单位名称
 */
object Unit {
    /**
     * 血压相关
     */
    val bp = "mmHg" // 血压
    val heart = "bpm" // 心率

    /**
     * 动脉硬化相关
     */
    val AVI = "" // 中心动脉硬化指数
    val API = "" // 上臂动脉硬化指数
    val C = "mL/mmHg" // 血管顺应性

    /**
     * 无创血糖相关
     */
    val bloodO2 = "%" // 血氧
    val hemoglobin = "g/L" // 血红蛋白
    val bloodFlow = "mm/s" // 血流速
    val bloodSugar = "mmol/L" // 血糖

    /**
     * 心率相关
     */
    val fatigueIndex = "lfhf" // 劳累指数
    val pressureIndex = "sdnn" // 压力指数
    val healthIndex = "pnn50" // 健康指数

    /**
     * 体温相关
     */
    val bodyTemp = "℃" // 体温
}