    public final String AVI = "";             // 中心动脉硬化指数
    public final String API = "";             // 上臂动脉硬化指数
    public final String C = "mL/mmHg";        // 血管顺应性

    /**
     * 无创血糖相关
     */
    public final String bloodO2 = "%";        // 血氧
    public final String hemoglobin = "g/L";   // 血红蛋白
    public final String bloodFlow = "mm/s";   // 血流速
    public final String bloodSugar = "mmol/L";// 血糖

    /**
     * 心率相关
     */
    public final String fatigueIndex = "lfhf";    // 劳累指数
    public final String pressureIndex = "sdnn";   // 压力指数
    public final String healthIndex = "pnn50";    // 健康指数

}
